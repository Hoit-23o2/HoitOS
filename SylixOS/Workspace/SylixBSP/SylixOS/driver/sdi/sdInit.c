#define  __SYLIXOS_KERNEL
#include <SylixOS.h>
#include "s3csdi.h"
/*********************************************************************************************************
** 函数名称: sdDrvInit
** 功能描述: 初始化 SD 驱动库
** 输    入: NONE     适配器
** 输    出: NONE
** 返    回: ERROR CODE
*********************************************************************************************************/
void sdDrvInit (void)
{
    API_SdmLibInit();
    API_SdmSdioLibInit();

    API_SdMemDrvInstall();                                              /*  安装 SD 存储卡驱动          */
    API_SdioBaseDrvInstall();                                           /*  安装 SDIO 接口驱动          */

    s3csdiDrvInstall();
}
